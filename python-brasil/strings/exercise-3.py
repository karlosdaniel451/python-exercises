"""
Nome na vertical. Faça um programa que solicite o nome do usuário e imprima-o na vertical.
"""

def print_string_in_vertical(string: str) -> None:
    for letter in string:
        print(letter)



print_string_in_vertical('karlos')
print('')

print_string_in_vertical('asdf')
print('')

print_string_in_vertical('342332')
print('')

